#[inline]
pub fn to_lower_encoded(c: char) -> (u8, [u8; 4]) {
    match bsearch_case_table_encoded(c, LOWERCASE_TABLE_ENCODED) {
        None => {
            let mut buf = [0u8; 4];
            let s = c.encode_utf8(&mut buf);
            (s.len() as u8, buf)
        }
        Some(index) => (
            LOWERCASE_TABLE_ENCODED[index].1,
            LOWERCASE_TABLE_ENCODED[index].2,
        ),
    }
}

fn bsearch_case_table_encoded(c: char, table: &[(char, u8, [u8; 4])]) -> Option<usize> {
    table.binary_search_by(|&(key, _, _)| key.cmp(&c)).ok()
}

